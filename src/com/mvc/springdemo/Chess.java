package com.mvc.springdemo;

public class Chess {
	
	
	private String ourResult;
	private String opponentResult;
	private String win;
	private String kval;
	double result;
	
	
	public Chess() {
		
	}


	public String getOurResult() {
		return ourResult;
	}


	public void setOurResult(String ourResult) {
		this.ourResult = ourResult;
	}


	public String getOpponentResult() {
		return opponentResult;
	}


	public void setOpponentResult(String opponentResult) {
		this.opponentResult = opponentResult;
	}


	public String getWin() {
		return win;
	}


	public void setWin(String win) {
		this.win = win;
	}


	public String getKval() {
		return kval;
	}


	public void setKval(String kval) {
		this.kval = kval;
	}


	public double getResult() {
		return result;
	}


	public void setResult(double result) {
		this.result = result;
	}

	
}
